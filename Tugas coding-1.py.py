apel = 10
jeruk = 3

# Penjumlahan
hasil = apel + jeruk
print(apel, '+', jeruk, '=', hasil)

# Pengurangan
hasil = apel - jeruk
print(apel, '-', jeruk, '=', hasil)

# Perkalian
hasil = apel * jeruk
print(apel, '*', jeruk, '=', hasil)

# Pembagian
hasil = apel / jeruk
print(apel, '/', jeruk, '=', hasil)

# Modulus atau sisa bagi
hasil = apel % jeruk
print(apel, '%', jeruk, '=', hasil)

# Eksponen atau pangkat
hasil = apel ** jeruk
print(apel, '^', jeruk, '=', hasil)

# Floor division (pembagian bulat ke bawah)
hasil = apel // jeruk
print(apel, '//', jeruk, '=', hasil)
